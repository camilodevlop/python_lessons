# Python is dynamically typed.
my_variable = "Python, hello world"
print("\nUsing a string\n", my_variable)
print(my_variable)
print(my_variable)

# Using an integer.
my_variable = 4
print("\nUsing integers\n", my_variable)
print(my_variable)

# Operations.
x = 10
y = 2
z = x + y
print("x is:",x,"| y is:",y,"\n",x,"+",y,"=",z)

# Memory addresses using the id() function.
print("\nMemory addresses using the id() function")
print("x is stored in ",id(x))
print("y is stored in ",id(y))
print("z is stored in ",id(z))
